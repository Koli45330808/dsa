/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public List<Integer> inorderTraversal(TreeNode root) {
        ArrayList<Integer> sc=new ArrayList<>();
        inorder(root,sc);
        return sc;
        
        
    }

    private  void inorder(TreeNode node, List<Integer> sc){
        if(node==null){
            return;
        }
        inorder(node.left,sc);
        sc.add(node.val);
        inorder(node.right,sc);
    }
}